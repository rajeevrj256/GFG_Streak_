class Solution {
  public:
    int kthElement(vector<int>& a, vector<int>& b, int k) {
        // code here
        int n=a.size();
        int m=b.size();
        vector<int>final(n+m);
        for(int i=0;i<n;i++){
            final[i]=a[i];
        }
        for(int i=n;i<n+m;i++){
            final[i]=b[i-n];
        }
        
        sort(final.begin(),final.end());
        
        return final[k-1];
        
    }
};

